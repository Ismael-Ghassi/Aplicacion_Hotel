    @Override
    public void actionPerformed(ActionEvent e) {
        /**
         * If you press this button you can go back to the previous frame.
         */
        if(e.getSource()==volver){
            try {
                framePrin = new Pantalla_Principal();
                framePrin.setVisible(true);
                this.setVisible(false);
            } catch (SQLException ex) {
                Logger.getLogger(Reservar.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            /**
             * If you press this button you will reserve a room.
             */
        }else if(e.getSource()==reservar){
            try {
                
                if(compDni(dniF.getText())){
                    bd = new AccesoBD("hotel_interfaces");
                
                if(alta.isSelected()){
                    bd.insertarReserva(nombreF.getText().toString(), dniF.getText().toString(), conocidoF.getSelectedItem().toString(), alta.getText().toString(), Integer.parseInt(idHotel.getText().toString()));
                    nombreF.setText("");
                    dniF.setText("");
                    JOptionPane.showMessageDialog(this, "La habitacion se ha reservado.");
                }else{
                    bd.insertarReserva(nombreF.getText().toString(), dniF.getText().toString(), conocidoF.getSelectedItem().toString(), baja.getText().toString(), Integer.parseInt(idHotel.getText().toString()));
                    nombreF.setText("");
                    dniF.setText("");
                    JOptionPane.showMessageDialog(this, "La habitacion se ha reservado.");
                }
                }else{
                    JOptionPane.showMessageDialog(this, "Introduce un dni correcto.", "Error", JOptionPane.ERROR_MESSAGE);

                }
                
                
                
            } catch (SQLException ex) {
                Logger.getLogger(Reservar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
